# Arithmetic operators (+, -, *, /, //, %, **)

# Addition
a = 10
b = 5
addition = a + b
print("Addition:", addition)

# Subtraction
subtraction = a - b
print("Subtraction:", subtraction)

# Multiplication
multiplication = a * b
print("Multiplication:", multiplication)

# Division
division = a / b
print("Division:", division)

# Floor Division
# example: 10 // 3 will give 3, because it rounds down to the nearest whole number
# example: 98 // 10 will give 9, because it rounds down to the nearest whole number
floor_division = a // b
print("Floor Division:", floor_division)

# Modulus
modulus = a % b
print("Modulus:", modulus)

# Exponentiation
exponentiation = a ** b
print("Exponentiation:", exponentiation)

print(98 // 10)
