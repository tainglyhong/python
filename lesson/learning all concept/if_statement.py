# IF conditional 

age = 18

if age >= 18:
    print("You are an adult.")
    
number = 10
if number == 10:
    print("The number is 10.")
    
price = 18.88
if price > 20:
    print("The price is greater than 20.")

is_true = True
if is_true:
    print("The condition is true.")

is_false = False
if is_false != True:
    print("The condition is false.")
    
    
# if-else statement
if age >= 18:
    print("You are an adult.")
else:
    print("You are a minor.")
    
if number == 10:
    print("The number is 10.")
else:
    print("The number is not 10.")
    
if price > 20:
    print("The price is greater than 20.")
else:
    print("The price is not greater than 20.")
    
#elif statement
