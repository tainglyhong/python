# logical operations (and, or, not)

result1 = True and False
result2 = True and True
print(result1)  
print(result2)  

result3 = True or False
result4 = False or False
result5 = True or True
print(result3)  # Output: True
print(result4)  # Output: False
print(result5)  # Output: True

result6 = not True
result7 = not False
print(result6)  # Output: False
print(result7)  # Output: True