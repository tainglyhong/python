# operator	operation	            example	    read as
# +	        addition	            x + y	    x plus y
# -	        subtraction	            x - y	    x minus y
# *	        multiplication	        x * y	    x times y
# **	    exponentiation	        x ** y	    x to the power of y
# /	        float division	        x / y	    x divided by y
# //	    floor division	        x // y	    x floor divided by y
# %	        modulus (remainder)	    x % y	    x modulo y

# example
x = 10
y = 20

# print("The addition is : " + str(x + y))
print(f"The addition is : {x + y}")

print(f"The subtraction is : {x - y}")

print(f"The multiply is : {x * y}")

print(f"The power is : {2 ** 3}")

print(f"The float division is : {x / y}")

print(f"The floor division : {x // y}")

print(f"The next floor division : {20 // 9}")

print(f"The modulus is : {x % y}")


