# https://docs.python.org/3.11/library/functions.html

# abs() - absolute value
print(abs(-4))
print(abs(-94.89))


# bin() - binary representation
print(bin(20))

# len() - length of an object
print(len("hello"))


# max() - maximum value
print(max(9,4))
# min() - minimum value
print(min(9,4))
# round() - round a number to a given precision
print(round(19.91199,2))

