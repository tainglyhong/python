# A tuple in Python is an ordered collection of values, 
# similar to a list, but immutable: once created, you cannot change its items.

my_tuple = (10, 20, 30)

student = ("Ali", 20, "Computer Science", 3.8)

# Creating Tuples
t1 = (1, 2, 3)
t2 = ("apple", "banana")
t3 = ()              # empty tuple
t4 = (5,)            # one-item tuple, comma is required

# Accessing Items
numbers = (10, 20, 30, 40)

print(numbers[0])    # 10
print(numbers[2])    # 30
print(numbers[-1])   # 40
